function[cp] = cpAIR(t)
    % caculate cp of air in temperature t, t should be in centigrade degree
    % return: cp in kJ/(kg * K)
    ts = [-50:10:100, 120:20:200, 250:50:400, 500:100:1200];
    cps = [1.013, 1.013, 1.013, 1.009, 1.009, 1.005, 1.005, 1.005, 1.005, 1.005, 1.005, 1.005, 1.009, 1.009, 1.009, 1.009, 1.009, 1.013, 1.017, 1.022, 1.026, 1.038, 1.047, 1.059, 1.068, 1.093, 1.114, 1.135, 1.156, 1.172, 1.185, 1.197, 1.210];
    
    cp = interp1(ts, cps, t, '*spline');
end